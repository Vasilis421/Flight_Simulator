function y = Cy_deltarr(x)
 
 if x >= 0
     c = [0 0 2.5204 -2.5506 0.6226 0.0895 8E-05];
     p = [x.^ 6, x.^ 5, x.^ 4, x.^ 3, x.^ 2, x.^ 1, x.^ 0]';
 else
     c = [0 0 -2.5204 -2.5506 -0.6226 0.0895 -8E-05];
     p = [x.^ 6, x.^ 5, x.^ 4, x.^ 3, x.^ 2, x.^ 1, x.^ 0]';
 end
 
  y = c * p;
  
end
